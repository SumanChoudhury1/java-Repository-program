
public class Switch {

	public static void main(String[] args) {
		int key=4;
		switch(key) {
		case 1:
			System.out.println(100);
			break;
			
		case 2:
			System.out.println(1000);
			break;
			
		case 3:
			System.out.println(10000);
			break;
			
		case 4:
			System.out.println(100000);
			break;
			
			default:
				System.out.println(400);
				break;
		}

	}

}
